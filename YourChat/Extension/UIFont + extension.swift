import UIKit

extension UIFont {
   static func avenir20() -> UIFont? {
      return UIFont.init(name: "avanit", size: 20)
   }
   
   static func avanir26() -> UIFont? {
      return UIFont.init(name: "anavir", size: 26)
   }
}
